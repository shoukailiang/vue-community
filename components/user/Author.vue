<template>
  <section id="author">
    <el-card class="" shadow="never">
      <div slot="header">
        <span class="has-text-weight-bold">👨‍💻 关于作者</span>
      </div>
      {{ user.nickName }}
      <el-row>
        <el-button
          :disabled="!$store.state.userInfo"
          :plain="!isFocus"
          @click="handleFocus"
          type="primary"
          size="medium"
        >
          {{ this.isFocus ? "已关注" : "关注" }}
        </el-button>
      </el-row>
    </el-card>
  </section>
</template>

<script>
export default {
  async created() {
    var focusData = {
      name: 111,
    };
    // 1. 取消关注，-1关注
    focusData.userId = this.$store.state.userInfo
      ? this.$store.state.userInfo.uid
      : "";
    focusData.focusId = this.user ? this.user.id : "";
    // 查询分类和标签
    if (this.$store.state.userInfo) {
      const { data } = await this.$isFocus(focusData);
      if (data) {
        this.isFocus = true;
      } else {
        this.isFocus = false;
      }
    }
  },
  props: {
    user: {
      type: Object,
    },
  },
  data() {
    return {
      isFocus: false,
      isFocusData: "",
    };
  },
  methods: {
    async handleFocus() {
      var focusData = {
        name: 111,
      };
      // 取消关注或者关注
      this.isFocus = !this.isFocus;
      focusData.userId = this.$store.state.userInfo
        ? this.$store.state.userInfo.uid
        : "";
      focusData.focusId = this.user ? this.user.id : "";
      const { code } = await this.$focusUser(focusData);
      if (code === 20000) {
        console.log("关注成功");
      }
    },
  },
  data() {
    return {
      isFocus: false,
    };
  },
};
</script>

